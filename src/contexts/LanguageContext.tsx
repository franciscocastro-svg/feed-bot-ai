type LanguageContextValue = {
  language: UiLanguage;
  locale: string;
  setLanguage: (language: UiLanguage) => void;
  t: (source: string) => string;
};

const LanguageContext = createContext<LanguageContextValue>({
  language: "pt-BR",
  locale: "pt-BR",
  setLanguage: () => undefined,
  t: source => source,
});

function initialLanguage(): UiLanguage {
  if (typeof window === "undefined") return "pt-BR";
  const stored = window.localStorage.getItem(STORAGE_KEY);
  return stored === "en-US" ? "en-US" : "pt-BR";
}

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<UiLanguage>(initialLanguage);

  const setLanguage = useCallback((next: UiLanguage) => {
    setLanguageState(next);
  }, []);

  useEffect(() => {
    window.localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language;
  }, [language]);

  const t = useCallback((source: string) => language === "en-US" ? (en[source] ?? source) : source, [language]);
  const value = useMemo(() => ({ language, locale: language, setLanguage, t }), [language, setLanguage, t]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  return useContext(LanguageContext);
}

export const UI_LANGUAGE_STORAGE_KEY = STORAGE_KEY;
